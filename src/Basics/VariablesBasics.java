package Basics;

public class VariablesBasics {
    public static void main(String[] args) {
        int x = 7;
        System.out.println(x);
        x = x - 2;
        System.out.println(x);
        x = x + 20;
        System.out.println(x);
        x = x - 30;
        System.out.println(x);
        x = x * 10;
        System.out.println(x);
        x = x / 25;
        System.out.println(x);
        int y;
        y = 20;
        System.out.println(y);
        y = y - x;
        System.out.println(y);
    }
}
